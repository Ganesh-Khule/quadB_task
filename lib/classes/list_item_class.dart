class ListItem {
  final String movieName;
  final String image;
  final String language;
  final String time;
  final int length;
  final String rating;
  final String description;

  ListItem({
    required this.movieName,
    required this.description,
    required this.image,
    required this.language,
    required this.time,
    required this.length,
    required this.rating,
  });
}
